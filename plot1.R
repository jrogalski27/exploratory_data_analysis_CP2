NEI <- readRDS("summarySCC_PM25.rds")
SCC <- readRDS("Source_Classification_Code.rds")
nei99 <- subset(NEI,year == 1999)
nei02 <- subset(NEI,year == 2002)
nei05 <- subset(NEI,year == 2005)
nei08 <- subset(NEI,year == 2008)
years <- unique(NEI$year)
pe99 <- sum(nei99$Emissions)
pe02 <- sum(nei02$Emissions)
pe05 <- sum(nei05$Emissions)
pe08 <- sum(nei08$Emissions)
pe25all <- c(pe99,pe02,pe05,pe08)

png("Plot1.png", width = 480, height = 480, units = "px")
plot(years,pe25all, type = "b",xlab = "", ylab = "Total PM2.5 Concentration (tons)", main = "PM2.5 Concentration in United States", col = "blue")
