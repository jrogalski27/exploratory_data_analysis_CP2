NEI <- readRDS("summarySCC_PM25.rds")
SCC <- readRDS("Source_Classification_Code.rds")
nei99balt <- subset(NEI,year == 1999 & fips == "24510")
nei02balt <- subset(NEI,year == 2002 & fips == "24510")
nei05balt <- subset(NEI,year == 2005 & fips == "24510")
nei08balt <- subset(NEI,year == 2008 & fips == "24510")
years <- unique(NEI$year)
pe99balt <- sum(nei99balt$Emissions)
pe02balt <- sum(nei02balt$Emissions)
pe05balt <- sum(nei05balt$Emissions)
pe08balt <- sum(nei08balt$Emissions)
pe25allbalt <- c(pe99balt,pe02balt,pe05balt,pe08balt)

png("Plot2.png", width = 480, height = 480, units = "px")
plot(years,pe25allbalt, type = "b",xlab = "", ylab = "Total PM2.5 Concentration (tons)", main = "PM2.5 Concentration in Baltmore City", col = "blue")
